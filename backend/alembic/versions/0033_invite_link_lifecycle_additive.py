# Discovered at write time on prod-like data — neither name is guaranteed by
# our SQLAlchemy model; both legacy snapshots have shipped under different
# autogenerated names. Drop by introspection in the upgrade body.
_CREATED_BY_FK_LOOKUP = """
    SELECT conname
    FROM pg_constraint
    WHERE conrelid = 'platform.identity_invite_links'::regclass
      AND contype = 'f'
      AND pg_get_constraintdef(oid) ILIKE '%(created_by)%'
      AND pg_get_constraintdef(oid) ILIKE '%platform.users%'
    LIMIT 1
"""


